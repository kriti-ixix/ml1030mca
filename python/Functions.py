def addNumbers():
    print(5 + 10)

def subNumbers(x, y):
    print(x - y)

def multiplyNumbers(x, y, z):
    return x * y * z


addNumbers()
subNumbers(100, 200)
p1 = multiplyNumbers(5, 10, 15)
p2 = multiplyNumbers(10, 20, 30)

print("The products are", p1, p2)

